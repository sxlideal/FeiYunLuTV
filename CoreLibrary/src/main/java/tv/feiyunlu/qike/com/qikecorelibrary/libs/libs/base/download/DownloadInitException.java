package tv.feiyunlu.qike.com.qikecorelibrary.libs.libs.base.download;

public class DownloadInitException extends Exception {

	public DownloadInitException() {
		super();
	}

	public DownloadInitException(String detailMessage, Throwable throwable) {
		super(detailMessage, throwable);
	}

	public DownloadInitException(String detailMessage) {
		super(detailMessage);
	}

	public DownloadInitException(Throwable throwable) {
		super(throwable);
	}

}
